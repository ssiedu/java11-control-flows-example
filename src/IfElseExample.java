
public class IfElseExample {
    public static void main(String[] args) {

        int marks=24;
        
        if(marks>=60){
            System.out.println("I-Division");
        }else if(marks>=45){
            System.out.println("II-Division");
        }else if(marks>=33){
            System.out.println("III-Division");
        }else{
            System.out.println("Fail");
        }
        
    }
}
