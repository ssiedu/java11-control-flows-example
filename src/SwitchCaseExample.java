
public class SwitchCaseExample {
    public static void main(String[] args) {
        int n=32;
        switch(n){
            case 1:
                System.out.println("ONE");
            break;
            case 2:
                System.out.println("TWO");
            break;
            case 3:
                System.out.println("THREE");
            break;
            default:
                System.out.println("DEFAULT CASE");
        }
        
    }
}
